import Logging
import NIOCore
import TecoSigner

extension StaticCredential: CredentialProvider {
    /// Return static credential.
    public func getCredential(on eventLoop: EventLoop, logger: Logger) -> EventLoopFuture<Credential> {
        eventLoop.makeSucceededFuture(self)
    }
}
